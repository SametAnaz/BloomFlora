/**
 * Block List Component
 * Shows available blocks that can be added to the page
 */

'use client';

import { useEffect, useState } from 'react';

import { getAllModuleMeta, initializeModules, type ModuleMeta } from '@/modules';

interface BlockListProps {
  onAddBlock: (moduleId: string, version: string) => void;
}

export function BlockList({ onAddBlock }: BlockListProps) {
  const [modules, setModules] = useState<ModuleMeta[]>([]);

  useEffect(() => {
    // Initialize modules on client side
    initializeModules();
    setModules(getAllModuleMeta());
  }, []);

  return (
    <div className="space-y-4">
      <h3 className="px-4 text-sm font-semibold">Bloklar</h3>
      <div className="space-y-1 px-2">
        {modules.length === 0 ? (
          <p className="px-3 py-2 text-sm text-muted-foreground">Yükleniyor...</p>
        ) : (
          modules.map((module) => {
            // Parse moduleId.version format
            const [moduleId, version] = module.id.split('.');
            return (
              <button
                key={module.id}
                onClick={() => onAddBlock(moduleId, version)}
                className="flex w-full items-center gap-3 rounded-md px-3 py-2 text-sm hover:bg-accent"
              >
                <div className="flex h-8 w-8 items-center justify-center rounded bg-primary/10 text-primary">
                  <ModuleIcon moduleId={moduleId} />
                </div>
                <div className="text-left">
                  <div className="font-medium">{module.name}</div>
                  <div className="text-xs text-muted-foreground line-clamp-1">{module.description}</div>
                </div>
              </button>
            );
          })
        )}
      </div>
    </div>
  );
}

